#include "Data.hpp"

/* ************************************************************************** */
/*   orthodox canonical form                                                  */
/* ************************************************************************** */

Data::Data(int num)
	: _num(num)
{}

Data::Data(const Data& other)
	: _num(other._num)
{}

Data&	Data::operator=(const Data& other)
{
	if (this != &other)
		_num = other._num;
	return *this;
}

Data::~Data()
{}

/* ************************************************************************** */
/*   methods                                                                  */
/* ************************************************************************** */

int	Data::num() const
{
	return _num;
}
